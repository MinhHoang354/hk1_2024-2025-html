function submitFormDataProduct() {

    let valueNameProduct = document.getElementById('product-name').value
    let valuePriceProduct = document.getElementById('product-price').value

    let valueQuantityProduct = document.getElementById('product-quantity').value
    let valuepatternProduct = document.getElementById('product-pattern').value
    let valueImageProduct = document.getElementById('image-pd').getAttribute("src")
    let valueDescriptionProduct = document.getElementById('product-description').value
    if (valueDescriptionProduct == '') {
        document.getElementById('error-product-description').innerHTML = 'Nhập mô tả'

        return false
    } else {
        document.getElementById('error-product-description').innerHTML = ''
    }

    if (valueNameProduct == '') {
        document.getElementById('error-product-name').innerHTML = 'Nhập tên sản phẩm'

        return false
    } else {
        document.getElementById('error-product-name').innerHTML = ''
    }

    if (valuePriceProduct == '') {
        document.getElementById('error-product-price').innerHTML = 'Nhập giá tiền'

        return false
    }
    if (isNaN(valuePriceProduct) || Number(valuePriceProduct) <= 0) {
        document.getElementById('error-product-price').innerHTML = 'Giá trị là số và lớn hơn 0'

        return false
    }

    else {
        document.getElementById('error-product-price').innerHTML = ''
    }

    if (valueQuantityProduct == '') {
        document.getElementById('error-product-quantity').innerHTML = 'Nhập số lượng'

        return false
    }

    else {
        document.getElementById('error-product-quantity').innerHTML = ''
    }


    if (valuepatternProduct == 'chonmau') {
        document.getElementById('error-product-pattern').innerHTML = 'Chọn mẫu'
        return false
    } else {
        document.getElementById('error-product-pattern').innerHTML = ''
    }

    if (valueImageProduct == '../assets/img/placeholder-image.jpg') {
        document.getElementById('error-product-image').innerHTML = 'Chọn ảnh'
        return false
    } else {
        document.getElementById('error-product-image').innerHTML = ''
    }

    let arrayProduct = []

    if ((localStorage.getItem('arrayProduct'))) {
        arrayProduct = JSON.parse(localStorage.getItem('arrayProduct'))
    }

    let itemProduct = {
        name: '',
        image: '',
        id: (new Date().getTime()),
        price: '',
        quantity: '',
        pattern: '',
        description: '',
    }
    itemProduct.name = valueNameProduct
    itemProduct.image = valueImageProduct
    itemProduct.price = valuePriceProduct
    itemProduct.quantity = valueQuantityProduct
    itemProduct.pattern = valuepatternProduct
    itemProduct.description = valueDescriptionProduct
    arrayProduct.push(itemProduct)

    localStorage.setItem('arrayProduct', JSON.stringify(arrayProduct));

    showProducts(1)

    window.location.reload(true)

}
